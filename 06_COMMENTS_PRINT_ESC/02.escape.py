print("Hello, this is KernalPrab.\nI am learning Python.")

# \t : Tab

print("Hello, How are you?\tI am fine.")

# \\ :  Backslash

print("You can call me Prabhat\\Sahil")

# \" : Double quote

print("I want to say \"I love my India\"")

# \' : Single quote

print("The word \'Love\' denotes my patriotism.")