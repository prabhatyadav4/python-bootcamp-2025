# Logical Operators

a = True
b = False

# AND Operator

print("AND Operator:")
print(a and a)
print(a and b)
print(b and a)
print(b and b, end="\n\n")

# OR Operator

print("OR Operator:")
print(a or a)
print(a or b)
print(b or a)
print(b or b, end="\n\n")

# NOT Operator

print("NOT Operator:")
print(not(a))
print(not(b))