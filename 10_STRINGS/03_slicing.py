name = "KernalPrab"

print(name[2:4])        #goes from 0 to 4-1 i.e. 0

print(name[2:-6])       #same as name[2:4]

#print(name[2:4:n])     skip n-1 characters

print(name[2:10:1])      #skip 0 character
print(name[2:10:2])      #skip 1 character
print(name[2:10:3])      #skip 2 character

print(name[:4])          #Replace the first empty number with 0 i.e. name[0:4]
print(name[1:])          #Replace the second empty number with length i.e. name[1:10]
