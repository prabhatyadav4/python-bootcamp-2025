# List containing 5 marks of students under 100
marks = [85, 92, 78, 64, 89]

# List containing mixed data types
mixed = [42, "Hello", True, 3.14, "Python"]

print("Marks:", marks)
print("Mixed Data:", mixed)

print(marks[0])  # Accessing first element
print(marks[1])  # Accessing second element

print(mixed[0])  # Accessing first element
print(mixed[1])  # Accessing second element 

print(marks[0:3])  # Slicing the first three elements
print(marks[1:4])  # Slicing from second to fourth elements 

print(mixed[0:3])  # Slicing the first three elements
print(mixed[1:4])  # Slicing from second to fourth elements